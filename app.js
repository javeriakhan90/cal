function age(){
var pera = document.getElementById("ab");
var way = document.getElementById("br");
var dob = new Date(way.value);
var dobmili = dob.getTime();
var today = new Date();
var todaymili = today.getTime();
var diff = todaymili - dobmili;
var accuage = Math.floor(diff/(1000*60*60*24*30*12))
way.innerHTML.value = " "
pera.innerHTML = (accuage)
}






























































































































